char	*ft_strcat(char *dest, char *src)
{
	int	i;

	i = 0;
	while (dest[i])
	{
		i += 1;
	}
	while (*src)
	{
		dest[i] = *src;
		i += 1;
		src += 1;
	}
	dest[i] = 0;
	return (dest);
}