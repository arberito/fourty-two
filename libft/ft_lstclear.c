#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*coming;

	if (lst == NULL)
	{
		return ;
	}
	while (*lst)
	{
		coming = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = coming;
	}
	*lst = NULL;
}
